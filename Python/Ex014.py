#Python Exercise #014 – Temperature Converter (Celsius to Fahrenheit)
#This program converts a temperature given in Celsius (°C) to Fahrenheit (°F) 🌡️.  
#The user inputs a temperature in Celsius, and the program applies the conversion formula:  
#   Fahrenheit = (Celsius × 9/5) + 32  
#The result is then displayed as the equivalent temperature in Fahrenheit.
# 

c = float(input("Enter here the temperature in ncelsius: C")) 
f = (c * 9/5) + 32
print(f"The temperature of {c}°C is equivalent to {f}°F")
 